package Ejercicio9.Utilidades;

public class Utilidad {

    // Utilidad para retornar el mayor entre dos números
    public static double esMayor(double a, double b) {
        double mayor = Math.max(a, b);
        return mayor;
    }

    // Utilidad para retornar el menor entre dos números
    public static double esMenor(double a, double b) {
        double menor = Math.min(a, b);
        return menor;
    }

    //

}
